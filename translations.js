document.querySelectorAll('input[name="language"]').forEach((elem) => {
    elem.addEventListener('change', function() {
        translatePage(this.value);
    });
});


function translatePage(language) {
    const elementsToTranslate = document.querySelectorAll('[id], [class]');

    elementsToTranslate.forEach(element => {
        const translationKeys = [];
        
        // Add the id to the list of keys if it exists
        if (element.id) {
            translationKeys.push(element.id);
        }

        // Add each class name to the list of keys
        element.classList.forEach(cls => {
            translationKeys.push(cls);
        });

        // Loop through all potential keys to find a matching translation
        translationKeys.forEach(translationKey => {
            const translation = translations[language][translationKey];
            if (translation) {
                if (element.tagName.toLowerCase() === 'input' || element.tagName.toLowerCase() === 'textarea') {
                    element.placeholder = translation;
                } else if (element.tagName.toLowerCase() === 'select' && element.id === 'employeeId') {
                    element.options[0].text = translation;
                } else {
                    element.innerHTML = translation;
                }
            }
        });
    });
}

translatePage('en');